    <h1>Tanjore Painting: The Rich South Indian Artform That Stood the Test of Time</h1>
    <p>Tanjore painting is a classical South Indian painting style, originating in the town of Thanjavur in TamilNadu. Thanjore paintings are characterized by rich and vivid colors, simple iconic composition, glittering gold foils overlaid on delicate but extensive gesso work and inlay of glass beads and pieces or very rarely precious and semi-precious gems. In Thanjavur paintings one can see the influence of Deccani, Vijayanagara, Maratha and even European or Company styles of painting. Thanjavur paintings continue to be made even to the present day, though not with the rigour and virtuosity that marked the paintings of yore.</p>

    <h2>HISTORY</h2>
    <p>Tanjore painting drew inspiration from Indian art of the 16th century, when the Vijayanagara Rayas administered their vast kingdom in southern India through the Nayaka Governors. The Nayakas were great patrons of art and literature. In 1676, Maratha rule was established in the region, and Maratha rulers encouraged the flourish of art and artists. It was during this time, that Tanjore painting truly flourished and developed into the form and style in which we recognise it today.</p>

    <h2>TANJORE PAINTING IN CURRENT WORLD:</h2>
    <p>Today, Tanjore paintings still have a broad appeal. In recent times, they have been commercialized extensively, and can be found being sold even in street markets. Although the artform has stood the test of time and continues to be popular, the general decline in quality is disconcerting to many art lovers. What is heartening though, is that workshops and training camps are being held to ensure that the artform continues to thrive, while retaining all the rich, traditional and artistic elements that make up the essence of the artform of Tanjore painting.</p>

    <h2>TRADITIONAL DANCES OF TAMILNADU:</h2>
    <p>Dance forms of Tamil Nadu elaborates the various dance forms originated and practiced in the Tamil Nadu, the southernmost state of the India.</p>

    <h3>BHARATANATYAM</h3>
    <p>Bharatanatyam is a major genre of Indian classical dance that originated in Tamil Nadu. It is one of the oldest classical dance forms of India and is one of the classical dance forms recognized by the Sangeet Natak Akademi. The name of the dance is a combination of the Tamil word Bharatam, a acronym with bha standing for bhavam (feelings, emotions), ra for ragam (melody, framework for musical notes), tam for talam (rhythm) and Natyam, a Sanskrit word for "dance". A description of the dance is found in the 2nd century CE Sangam literature of Silappatikaram, while temple sculptures from the early middle ages of 6th to 9th century CE project the dance form. The theoretical foundations of the dance are found in Kootha Nool in Tamil and in Natya Shastra, a Sanskrit text of performance arts. According to some Indologists, the dance is linked to the Devadasi culture prevalent in Hindu temples.</p>
    <p>A traditional Bharatanatyam dance performance follows a seven to eight-part order of presentation called Margams. The dance is characterized by the fixed upper torso with bent legs or flexed out knees combined with various footwork and a number of gestures known as abhinaya using various hand mudras, expressions using the eyes and other face muscles. The dance might be accompanied by music and a singer and typically a guru is present as the director and conductor of the performance and art.</p>

    <h3>Bommalattam</h3>
    <p>Bommalattam is a type of puppetry using inanimate objects, that originated in the region. While the origin of the art is uncertain, it has existed as an art form for years. It uses various doll marionettes which are 1–3 ft (0.30–0.91 m) tall and may weigh up to 10 kg (22 lb). The puppets are manipulated by rods and strings attached to the heads, back, arms and legs. The puppets are made of light weight wood and are dressed in crafted costumes bulked up by paper stuffings to give a more rounded appearance. The puppets are carefully crafted with prominent jewellery so that the edges are visible in the shadows.</p>
    <p>Puppeteers stand overhead in an area 4 ft (1.2 m) high and 11 ft (3.4 m) wide and operate the puppets by using movements from hands and arms to control devices the rods or strings. The puppet staging area is created by stretching a black cloth over a bench and the puppets are operated behind a white screen illuminated by a lantern or oil lamps. The audience are seated in front of the screen with the screen literally separating the performers and audience, who are able to view the shadows of the puppets which are operated by the puppeteers.</p>

    <h3>Bhagavatha nadanam</h3>
    <p>The dance form narrates the life and stories of the avatars of Hindu god Vishnu. It is usually performed during Navarathri or festivals like Gokulashtami.</p>

    <h3>Chakkai Attam</h3>
    <p>Chakkaiaattam is a form of folk dance practiced majorly in South Central parts of the state. It is a social dance performed by a group of men or women with no specific theme. The dance may be accompanied by music and songs and are performed usually as an invocation to Tamil gods such as Murugan or Mariamman. The dancers hold wooden sticks measuring about 7 in (180 mm) long and strung in a thread between the fingers which are used to produce different sounds.</p>

    <h3>Devaraattam</h3>
    <p>Devarattam is a folk dance form. Deverattam means "Dance of the Gods" in Tamil. The act is performed generally by males who wear costumes, make-up and may wear different face masks suiting the theme enacted. Even female characters are played by males. Devarattam is an abstract dance with no words used and the dancers use various expressions to express various themes, that is derived from the aspects of nature, animals and birds. It is performed at weddings, festivals and ritual occasions.</p>

    <h3>Kai silambattam</h3>
    <p>Silambu is a hollow anklet made up of a metal such as copper and filled with iron or silver beads that produce noise when the wearer moves or dances. Silambu forms the basis of the Tamil Sangam epic Silappathikaram. It is generally worn by women on the leg, termed as kālchilambu in Tamil in contrast to kāichilambu held in the hand. For the dance, the dancers wear anklets and hold a silambu in their hands to make noises while dancing. The dance is performed in temples during Amman festivals or Navaratri festival especially to praise female deities like Durga or Kali.</p>

    <h3>Karakattam</h3>
    <p>Karakattam is a folk dance involving dancers balancing clay or metal pot(s) on the head while making movements with the body. The pot may be empty or sometimes filled with water. The pot is decorated with colorful flowers and leaves mostly neem, which is of medicinal and religious importance to the Tamils. The performer may sometimes carry multiple pots layered on top of the other. This dance is usually associated with the worship of Amman. The art form was traditionally practiced by women who wore saris and men may join them, posing with different make-up as a part of the story line and played in festivals and fairs. In the 21st century, the dance has sometimes obtained a reputation for vulgarity as it has been subjected to significant changes.</p>

    <h3>Kavadiyattam</h3>
    <p>Kavadiattam is often a ceremonial act of sacrifice and offering to Hindu gods especially Murugan. Kavadi (meaning "burden" in Tamil) itself is a physical burden emphasizing debt bondage and by bearing the kavadi, the dancer implores the gods for assistance, usually on behalf of a loved one who is in need of healing. It is performed during the ceremonial worship of Murugan, the Tamil God of War and involves the ceremonial sacrifice by the dancer. The kavadi itself is a wooden structure that is carried balancing on the shoulders. Some types of kavadi are large wooden structures that are carried using the shoulder alone, while other types may contain hooks that pierce into the skin. The dancer undergoes rigorous physical training, ritual cleansing by maintaining celibacy, not eating non-vegetarian foods, bathing twice a day, and walking on shoes with nails. During the worship, the dancer wears a saffron cloth and is smeared with vibuthi. He may sometimes carry a milk pot which is poured on the idol at the end of the dance. The dancers dance in a trance invoking god to praise and seek the deities blessings. The dance is also performed in fairs and festivals especially those associated with Murugan temples.</p>

    <h3>Mayilattam</h3>
    <p>Mayilattam (peacock dance) is a ritual dance form performed by girls, where the performers tie a structure made of cardboard to the hip and it looks like a peacock.</p>

    <h3>Oyilattam</h3>
    <p>Oyilattam, also known as "dance of grace", is a folk dance with origins in Tamil Nadu. Oyilattam used to be a popular dance in the olden days performed by the people in villages during festivals and other celebrations. Oyilattam is mentioned in Silappatikaram. Oyilattam is usually performed by young men and women. In this dance, the performers wear traditional Tamil dresses and dance with intricate movements.</p>

    <h3>Thappaattam</h3>
    <p>Thappattam is a type of dance with the use of a hand drum (thappu) that is often played by men who make rhythmic music with the drum and dance to its tune. It is considered to be one of the oldest forms of traditional music and dance in Tamil Nadu. Thappattam is performed during festivals and various social occasions. The dance form has seen significant changes with both men and women performing the act in recent times.</p>

    <h3>Villu Paattu</h3>
    <p>Villu Paatu, also known as Villadichan Paattu, is an ancient form of musical story-telling where narration is interspersed with music, using a unique instrument resembling a bow. The villu (bow), a prominent weapon in ancient times, gives this art form its name. The tradition of Villu Paatu is said to be over 300 years old. The main storyteller narrates the story while artists play the villu for rhythm. The traditional stories are often about Hindu gods and goddesses. The songs are often humorous and bring out the flavour of rural Tamil culture.</p>
